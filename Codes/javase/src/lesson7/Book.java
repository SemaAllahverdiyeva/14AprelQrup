package lesson7;

public class Book {
	int id;
	String name;
	String author;
	int pageCount;
	String description;

	public Book(int id, String name, String author, int pageCount, String description) {
		this.id = id;
		this.name = name;
		this.author = author;
		this.pageCount = pageCount;
		this.description = description;
	}
}
